package com.watchcorn.watchcorn;

public class Artist {

    String name;
    String birthDate;
    String birthPlace;
    String imdbId;
    String bio;
    String height;
    String imageUrl;




    public String getName() {
        return name;
    }

    public String getBirthDate() {
        return birthDate;
    }

    public String getBirthPlace() {
        return birthPlace;
    }

    public String getImdbId() {
        return imdbId;
    }

    public String getBio() {
        return bio;
    }

    public String getHeight() {
        return height;
    }

    public String getImageUrl() {
        return imageUrl;
    }
}
